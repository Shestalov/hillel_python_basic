"""
По даному натуральному числу N знайдіть найбільшу міру 2-ки в ступені, що не перевищує N.
Виведіть показник ступеня і саму міру.
Операцією зведення на ступінь користуватися не можна!
"""

n = 10  # 50

for num in range(1, n + 1):

    res = 1

    for i in range(1, num + 1):
        res *= 2

    if res > n:
        print(num - 1, int(res / 2))
        break
